import { cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const packageRoot = path.join(root, 'node_modules', 'cryptocurrency-icons');
const sourceSvgRoot = path.join(packageRoot, 'svg');
const staticRoot = path.join(root, 'static', 'crypto-icons');
const manifestPath = path.join(packageRoot, 'manifest.json');
const outputModulePath = path.join(root, 'src', 'lib', 'cryptoIcons.ts');
const variants = ['color', 'black', 'white'];

if (!existsSync(manifestPath)) {
  throw new Error('cryptocurrency-icons is not installed. Run bun install first.');
}

await rm(staticRoot, { recursive: true, force: true });
await mkdir(staticRoot, { recursive: true });

for (const variant of variants) {
  await cp(path.join(sourceSvgRoot, variant), path.join(staticRoot, variant), { recursive: true });
}

const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));
const icons = manifest
  .map((item) => {
    const symbol = String(item.symbol ?? '').toLowerCase();
    const name = String(item.name ?? symbol.toUpperCase());
    const availableVariants = variants.filter((variant) =>
      existsSync(path.join(staticRoot, variant, `${symbol}.svg`))
    );

    return {
      symbol,
      name,
      variants: Object.fromEntries(
        availableVariants.map((variant) => [variant, `/crypto-icons/${variant}/${symbol}.svg`])
      )
    };
  })
  .filter((item) => item.symbol && Object.keys(item.variants).length > 0)
  .sort((a, b) => a.symbol.localeCompare(b.symbol));

const module = `// Generated by scripts/sync-crypto-icons.mjs from cryptocurrency-icons@0.18.1.
// Source license: CC0-1.0.
export type CryptoIconVariant = 'color' | 'black' | 'white';

export interface CryptoIcon {
  symbol: string;
  name: string;
  variants: Partial<Record<CryptoIconVariant, string>>;
}

export const cryptoIcons = ${JSON.stringify(icons, null, 2)} as const satisfies readonly CryptoIcon[];

const iconSymbols = new Set<string>(cryptoIcons.map((icon) => icon.symbol));

export function hasCryptoIcon(symbol: string): boolean {
  return iconSymbols.has(symbol.toLowerCase());
}

export function getCryptoIcon(symbol: string): CryptoIcon | undefined {
  const normalized = symbol.toLowerCase();
  return cryptoIcons.find((icon) => icon.symbol === normalized);
}

export function cryptoIconPath(symbol: string, variant: CryptoIconVariant): string | undefined {
  const icon = getCryptoIcon(symbol);
  return icon?.variants[variant] ?? icon?.variants.color;
}
`;

await writeFile(outputModulePath, module);
console.log(`Synced ${icons.length} cryptocurrency icons to ${path.relative(root, staticRoot)}.`);
